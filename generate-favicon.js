import fs from 'fs';

// Simple ICO format favicon - this is a basic 16x16 ICO file with the OnGrid Solar colors
const icoBuffer = Buffer.from([
    // ICO header
    0x00, 0x00, // Reserved (must be 0)
    0x01, 0x00, // Image type (1 = ICO)
    0x01, 0x00, // Number of images in file

    // Image directory entry
    0x10, // Width (16 pixels)
    0x10, // Height (16 pixels)
    0x00, // Number of colors (0 = no palette)
    0x00, // Reserved
    0x01, 0x00, // Color planes
    0x20, 0x00, // Bits per pixel (32-bit)
    0x00, 0x04, 0x00, 0x00, // Size of image data (1024 bytes)
    0x16, 0x00, 0x00, 0x00, // Offset to image data

    // Bitmap header (40 bytes)
    0x28, 0x00, 0x00, 0x00, // Header size
    0x10, 0x00, 0x00, 0x00, // Width
    0x20, 0x00, 0x00, 0x00, // Height (doubled for ICO format)
    0x01, 0x00, // Planes
    0x20, 0x00, // Bits per pixel
    0x00, 0x00, 0x00, 0x00, // Compression
    0x00, 0x04, 0x00, 0x00, // Image size
    0x00, 0x00, 0x00, 0x00, // X pixels per meter
    0x00, 0x00, 0x00, 0x00, // Y pixels per meter
    0x00, 0x00, 0x00, 0x00, // Colors used
    0x00, 0x00, 0x00, 0x00, // Important colors

    // Image data - simple 16x16 orange square with white center (representing solar panel)
    // Bottom row first (ICO format is bottom-up)
    ...Array(16).fill([
        0x33, 0xA6, 0xFA, 0xFF, // Orange background
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF,
        0x33, 0xA6, 0xFA, 0xFF
    ]).flat(),
    // AND mask (1 bit per pixel, all 0s for opaque)
    ...Array(32).fill(0x00)
]);

export default icoBuffer;